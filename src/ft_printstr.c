#include "ft_printf.h"

int	ft_printchar(int c)
{
	write(1, &c, 1);
	return (1);
}

int	ft_printstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (ft_printf("(null)"));
	while (str[i])
		i += ft_printchar(str[i]);
	return (i);
}
